#include <stdio.h>

int main() {
    // Declare a variable to store user input
    int userInput;

    // Prompt the user for input
    printf("Enter a number: ");

    // Use scanf to read user input
    scanf("%d", &userInput);

    // Display the entered input back to the user
    printf("You entered: %d\n", userInput);

    return 0;
}
